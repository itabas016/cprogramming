// * 3、编写C程序，实现输入一个英文字母，判断该字母是大写还是小写。
/* *************************************************** */

# include <stdio.h>
void question_3()
{
	char ch = 0;
	printf("请输入一个字母: ");
	scanf("%c", &ch);
	if (ch >= 'A' && ch <= 'Z') {
		printf("大写英文字母\n");
	}
	else if (ch >= 'a' && ch <= 'z') {
		printf("小写英文字母\n");
	}
	else {
		printf("不是英文字母\n");
	}
}
