# include <stdio.h>
void question_2()
{
	int x, y;
	scanf("%d", &x);
	if (x>0)
		y = x*x + 1;
	else if (x<0)
		y = x*x - 1;
	else
		y = 0;
	printf("y=%d\n", y);
}
